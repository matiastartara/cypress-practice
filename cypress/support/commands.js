Cypress.Commands.add('login', (username, password) => {

    //Set Username
    const user = cy.get('[name="username"]');
    user.clear();
    user.type(username);

    //Set Password
    const passwd = cy.get('[name="password"]');
    passwd.clear();
    passwd.type(password);

    //Submit
    cy.get('[type="submit"]').click();
})